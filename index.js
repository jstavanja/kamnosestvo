$(document).ready(() => {
  $('.navigation').height($(window).height())
  $('#fullpage').fullpage({
    anchors: ['page1', 'page2', 'page3', 'page4', 'page5'],
    menu: '#menu',
    slidesNavigation: true,
    scrollingSpeed: 1000,
    autoScrolling: true
  });

  setTimeout(() => {
    $('.website-container').addClass('navigation-open');
    $('.page-welcome .card').addClass('fadeInRight');
  }, 100);

  $('.close-modal').on('click', () => {
    closeModal('.modal');
  }) 
  
})

